import re
import json
import torch
from pathlib import Path
from datasets import load_dataset
from transformers import AutoTokenizer, AutoModelForCausalLM
from huggingface_hub import login
seed = 42
login()

MODEL_ID = "CohereLabs/tiny-aya-base"

tokenizer = AutoTokenizer.from_pretrained(MODEL_ID)

model = AutoModelForCausalLM.from_pretrained(
    MODEL_ID,
    torch_dtype=torch.bfloat16 if torch.cuda.is_available() else torch.float32,
    device_map="auto"
)
model.eval()
device = "cuda" if torch.cuda.is_available() else "cpu"

model.to(device)

XNLI_LABELS = {
    0: "entailment",
    1: "neutral",
    2: "contradiction",
}

def generate_new_text(prompt, max_new_tokens):
    inputs = tokenizer(prompt, return_tensors="pt", truncation=True, max_length=1024)
    inputs = {k: v.to(device) for k, v in inputs.items()}

    with torch.no_grad():
        outputs = model.generate(
            **inputs,
            max_new_tokens=max_new_tokens,
            do_sample=False,
            pad_token_id=tokenizer.eos_token_id
        )

    new_text = tokenizer.decode(
        outputs[0][inputs["input_ids"].shape[1]:],
        skip_special_tokens=True
    )
    return new_text.strip()

def parse_xnli_prediction(text):
    text = text.lower()
    for label in ["entailment", "neutral", "contradiction"]:
        if label in text:
            return label
    return None

def parse_mcq_letter(text):
    text = text.strip().upper()
    m = re.search(r"\b([ABCD])\b", text)
    if m:
        return m.group(1)
    return None

def parse_story_choice(text):
    text = text.strip()
    m = re.search(r"\b([12])\b", text)
    if m:
        return int(m.group(1))
    return None

def eval_xnli(lang, n):
    ds = load_dataset("facebook/xnli", lang, split="validation").shuffle(seed=42).select(range(n))
    correct = 0
    samples = []

    for ex in ds:
        gold = XNLI_LABELS[ex["label"]]
        prompt = f"""Decide the relationship between the premise and hypothesis.
Answer using exactly one word: entailment, neutral, or contradiction.

Premise: {ex['premise']}
Hypothesis: {ex['hypothesis']}

Answer:"""

        raw = generate_new_text(prompt, max_new_tokens=8)
        pred = parse_xnli_prediction(raw)
        correct += int(pred == gold)

        samples.append({
            "gold": gold,
            "pred": pred,
            "raw_output": raw
        })

    return {
        "benchmark": "xnli",
        "language": lang,
        "split": "validation",
        "n": len(ds),
        "metric": "accuracy",
        "score": correct / len(ds),
        "samples": samples[:3]
    }

def eval_xstorycloze_zh(n):
    ds = load_dataset("juletxara/xstory_cloze", "zh", split="train").shuffle(seed=42).select(range(n))
    correct = 0
    samples = []

    for ex in ds:
        gold = int(ex["answer_right_ending"])

        prompt = f"""Choose the more sensible ending for the story.
Answer with only 1 or 2.

Story:
1. {ex['input_sentence_1']}
2. {ex['input_sentence_2']}
3. {ex['input_sentence_3']}
4. {ex['input_sentence_4']}

Ending 1: {ex['sentence_quiz1']}
Ending 2: {ex['sentence_quiz2']}

Answer:"""

        raw = generate_new_text(prompt, max_new_tokens=4)
        pred = parse_story_choice(raw)
        correct += int(pred == gold)

        samples.append({
            "gold": gold,
            "pred": pred,
            "raw_output": raw
        })

    return {
        "benchmark": "xstorycloze",
        "language": "zh",
        "split": "train",
        "n": len(ds),
        "metric": "accuracy",
        "score": correct / len(ds),
        "samples": samples[:3]
    }

def eval_global_mmlu(lang, n):
    ds = load_dataset("CohereLabs/Global-MMLU", lang, split=f"test").shuffle(seed=42).select(range(n))
    correct = 0
    samples = []

    for ex in ds:
        gold = ex["answer"].strip().upper()

        prompt = f"""Answer the multiple choice question.
Reply with only one letter: A, B, C, or D.

Question: {ex['question']}
A. {ex['option_a']}
B. {ex['option_b']}
C. {ex['option_c']}
D. {ex['option_d']}

Answer:"""

        raw = generate_new_text(prompt, max_new_tokens=4)
        pred = parse_mcq_letter(raw)
        correct += int(pred == gold)

        samples.append({
            "gold": gold,
            "pred": pred,
            "raw_output": raw
        })

    return {
        "benchmark": "global_mmlu",
        "language": lang,
        "split": "test",
        "n": len(ds),
        "metric": "accuracy",
        "score": correct / len(ds),
        "samples": samples[:3]
    }

def main():
    results = []
    results.append(eval_xnli("zh", n=50))
    results.append(eval_xnli("ur", n=50))
    results.append(eval_xstorycloze_zh(n=50))
    results.append(eval_global_mmlu("zh", n=50))
    results.append(eval_global_mmlu("am", n=50))

    for r in results:
        print(f"{r['benchmark']} | {r['language']} | {r['metric']} = {r['score']:.3f}")

    script_dir = Path(__file__).resolve().parent
    save_dir = script_dir.parent / "results" / "baseline"
    save_dir.mkdir(parents=True, exist_ok=True)

    save_path = save_dir / "baseline_scores.json"

    with open(save_path, "w", encoding="utf-8") as f:
        json.dump(results, f, indent=2, ensure_ascii=False)

    print(f"Saved to {save_path}")

if __name__ == "__main__":
    main()